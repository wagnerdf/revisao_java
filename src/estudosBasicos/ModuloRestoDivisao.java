package estudosBasicos;

public class ModuloRestoDivisao {

	public static void main(String[] args) {
		
		
		/*Módulo: Resto da divisão %*/
		
		double carro = 9;
		double pessoa = 2;
		
		double resto = carro % pessoa; /*MOD*/
		
		System.out.println("Sobram exatamente : "+ resto +" Carros.");

	}

}
